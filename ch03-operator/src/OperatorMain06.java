public class OperatorMain06 {

	public static void main(String[] args) {
		
		boolean result;
		int a = 10;
		int b = 11;
		
		result = a > b;
		System.out.println("a > b : " + result);
		
		result = a < b;
		System.out.println("a < b : " + result);
		
		result = a >= b;
		System.out.println("a >= b : " + result);
		
		result = a < b;
		System.out.println("a <= b : " + result);
		
		result = a == b;
		System.out.println("a == b : " + result);
		
		result = a != b;
		System.out.println("a != b : " + result);

	}

}
